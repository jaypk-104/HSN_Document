SELECT F.BILL_NO, A.BL_DOC_NO, A.BL_NO , SUM(E.COG_GL_AMT)
FROM M_BL_HDR_DISTR A JOIN M_BL_DTL_DISTR B ON A.BL_NO = B.BL_NO
JOIN M_CC_DTL_DISTR C ON B.BL_NO = C.BL_NO AND B.BL_SEQ = C.BL_SEQ
JOIN M_GR_DISTR D ON C.CC_NO = D.CC_NO AND C.CC_SEQ = D.CC_SEQ
JOIN M_GR_TO_DN_DISTR E ON D.MVMT_NO = E.MVMT_NO
JOIN S_BILL_D_DISTR F ON E.DN_NO = F.DN_NO AND E.DN_SEQ = F.DN_SEQ
WHERE F.BILL_NO = 'BN2020033100018'
AND A.BL_NO = 'VB2019100400001'
GROUP BY A.BL_DOC_NO, A.BL_NO, F.BILL_NO;