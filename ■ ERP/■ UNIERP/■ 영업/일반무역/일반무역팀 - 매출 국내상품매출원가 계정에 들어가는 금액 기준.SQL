/*
매출전표 국내상품원가에 들어가는 전표 금액
*/
SELECT F.BL_NO, SUM(B.COG_GL_AMT)  AS 국내매출원가
FROM S_BILL_D_DISTR A JOIN M_GR_TO_DN_DISTR B ON A.DN_NO = B.DN_NO AND A.DN_SEQ = B.DN_SEQ
                      JOIN M_GR_DISTR C ON B.MVMT_NO = C.MVMT_NO
											JOIN M_CC_DTL_DISTR D ON C.CC_NO = D.CC_NO AND D.CC_SEQ = D.CC_SEQ
											JOIN M_BL_DTL_DISTR E ON D.BL_NO = E.BL_NO AND D.BL_SEQ = E.BL_SEQ
											JOIN M_BL_HDR_DISTR F ON E.BL_NO = F.BL_NO
WHERE A.BILL_NO = 'BN2020022900001'
GROUP BY F.BL_NO;


SELECT *
