/*
회계수불 기말에 잔액남아있을때 
M_IV_HDR에 A TEMP GL REF_NO 가 생성안된거 체크
아마 ERP ->MRO 로 데이터 넘겼다 삭제했는데 삭제가 안된거 
*/

SELECT DISTINCT IV_NO
FROM M_IV_DTL
WHERE IV_NO NOT IN (
SELECT  REF_NO
FROM A_TEMP_GL
WHERE SUBSTRING(REF_NO,1,8) = 'IM202007'
)
AND SUBSTRING(IV_NO,1,8) = 'IM202007'



SELECT *
fROM M_IV_HDR 
WHERE IV_DT BETWEEN '2020-07-01' AND '2020-07-31'
AND IV_NO NOT IN (
SELECT REF_NO
FROM A_TEMP_GL
WHERE SUBSTRING(REF_NO,1,2) = 'IM'
AND TEMP_GL_DT BETWEEN '2020-07-01' AND '2020-07-31'
)
AND SUBSTRING(IV_NO,1,2) = 'IM'


